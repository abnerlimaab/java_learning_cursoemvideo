    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        Painel.setVisible(true);
        
        int valor = Integer.parseInt(lblValor.getValue().toString());
        int resto = valor % 2;
        int quadrado = (int) Math.pow(valor, 2);
        int cubo = (int) Math.pow(valor, 3);
        float rq = (float) Math.sqrt(valor);
        float rc = (float) Math.cbrt(valor);
        int absoluto = Math.abs(valor);
        
        lblResto.setText(Integer.toString(resto));
        lblQuadrado.setText(Integer.toString(quadrado));
        lblCubo.setText(Integer.toString(cubo));
        lblRQ.setText(String.format("%.2f", rq));
        lblRC.setText(String.format("%.2f", rc));
        lblAbs.setText(Integer.toString(absoluto));
    }//GEN-LAST:event_btnCalcularActionPerformed

